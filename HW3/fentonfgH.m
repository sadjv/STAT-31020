function [f,g,H]=fentonfgH(x)

% syms h(x1,x2)
% 
% h(x1,x2) = 0.1*(12+x1^2+(1+x2^2)/x1^2+(x1^2*x2^2+100)/(x1*x2)^4);
% 
% f = h(x(1),x(2));
% gradx = diff(h,x1);
% grady = diff(h,x2);
% gradxx = diff(gradx,x1);
% gradxy = diff(gradx,x2);
% gradyx = diff(grady,x1);
% gradyy = diff(grady,x2);
% 
% g = [gradx(x(1),x(2)), grady(x(1),x(2))];
% H = [gradxx(x(1),x(2)),gradxy(x(1),x(2)); gradyx(x(1),x(2)),gradyy(x(1),x(2))];

a = x(1);
b = x(2);
f = 0.1*(12+a^2+(1+b^2)/a^2+(a^2*b^2+100)/(a*b)^4);
g1 = 0.1*(2*a-2*(1+b^2)/a^3-4*(a^2*b^2+100)/(a^5*b^4)+2*a*b^2/(a*b)^4);
g2 = 0.1*(2*b/a^2-4*(a^2*b^2+100)/(a^4*b^5)+2*a^2*b/(a*b)^4);
g = [g1,g2];
h1 = 0.1*(2+6*(1+b^2)/a^4+20*(a^2*b^2+100)/(a^6*b^4)-8/(a^4*b^2)+2/(a^4*b^2)-8/(a^4*b^2));
h2 = 0.1*(-4*(b)/a^3+16*(a^2*b^2+100)/(a^5*b^5)-8/(a^3*b^3)+4/(a^3*b^3)-8/(b^3*a^3));
h3 = 0.1*(-4*(b)/a^3+16*(a^2*b^2+100)/(a^5*b^5)-8/(a^3*b^3)+4/(a^3*b^3)-8/(a^3*b^3));
h4 = 0.1*(2/a^2+20*(a^2*b^2+100)/(b^6*a^4)-8/(b^4*a^2)+2/(b^4*a^2)-8/(b^4*a^2));
H = [h1,h2;h3,h4];
